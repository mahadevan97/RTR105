// loop(s)
// construction of loop while
// while(expression){actions}
//while(expression) action;
// while(expression){actions;}





#include <stdio.h>

int main()
{
    int a = 0;
    
    while(0)
    {
        printf("will we text(while(0))\n");//text will not be shown on the result
    }
    while(1)
    {
        printf("will we text(while(1))\n");//infinite loop
        break;
    }

    while(a<=20)
    {
        printf("a = %d\n",a);
        //a = a + 2;
        a += 2;
    }
    printf("after loop:\n");
    printf("a = %d\n",a);
    printf("0 <= 20 -result is %d\n", 0<=20);
    printf("22 <= 20 -result is %d\n", 22<=20);
    
    
    return 0;
}
